export const run = {
   usage: ['تعيين_الترحيب'], // الاسم العربي الرئيسي الذي سيظهر في القائمة (تعيين_الترحيب)
   hidden: ['setmsg', 'تغيير_الترحيب', 'رسالة_المنيو'], // الاختصارات المخفية لضمان عمل البوت كبدائل دائمًا
   use: 'النص الترحيبي الجديد مع الاختصارات',
   category: 'المطور', // الفئة المعربة لتنظيم قائمة أوامر المالك
   async: async (m, {
      client,
      text,
      isPrefix,
      command,
      Utils
   }) => {
      try {
         let setting = global.db.setting
         
         // إذا لم يتم إدخال نص، يتم عرض دليل الاستخدام التوضيحي والمعرب تلقائياً
         if (!text) return client.reply(m.chat, explain(isPrefix, command), m)
         
         // حفظ النص الترحيبي الجديد في قاعدة البيانات
         setting.msg = text
         client.reply(m.chat, Utils.texted('bold', `🚩 تم تعيين وتحديث رسالة ترحيب القائمة بنجاح.\n\n© DEV ABOODI OFFICIAL`), m)
      } catch (e) {
         client.reply(m.chat, Utils.jsonFormat(e), m)
      }
   },
   owner: true // الأداة محمية ومخصصة لمالك ومطور البوت فقط لحماية واجهة النظام
}

// دالة صياغة واجهة الدليل التعليمي للاختصارات المتاحة بالعربية
const explain = (prefix, command) => {
   return `⚙️ *دليل استخدام وتعيين رسالة ترحيب البوت* :\n
لا يمكن حفظ رسالة فارغة، يمكنك صياغة نص ترحيبي واستخدام الكلمات المفتاحية التالية ليقوم البوت باستبدالها تلقائياً لكل مستخدم:\n
*1.* +tag : لعمل إشارة (منشن) للحساب الذي استدعى القائمة.
*2.* +name : لعرض الاسم المسجل لحساب المستخدم.
*3.* +greeting : لعرض تحية وقتية تلقائية (مثل: صباح الخير / مساء الخير) حسب توقيت المستخدم.\n
• *مثال توضيحي للاستخدام* :
${prefix + command} أهلاً بك يا +name +greeting 🌟، أنا نظام برميجي مخصص لخدمتك.`
}
