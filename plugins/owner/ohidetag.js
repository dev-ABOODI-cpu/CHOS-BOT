export const run = {
   usage: ['تاغ_المطور'], // الاسم العربي الرئيسي الذي سيظهر في القائمة (تاغ_المطور)
   hidden: ['ohidetag', 'o', 'اشارة_مخفية'], // الاختصارات المخفية لضمان عمل البوت بالإنجليزية أو البدائل العربية
   use: 'النص المراد إرساله لتنبيه الأعضاء',
   category: 'المطور', // الفئة المعربة لتنظيم قائمة أوامر المالك
   async: async (m, {
      client,
      text,
      participants
   }) => {
      // جلب معرفات كافة الأعضاء المتواجدين في المجموعة حالياً
      let users = participants.map(u => u.id)
      
      // إرسال النص مع تضمين منشن مخفي لكافة الأعضاء لضمان وصول التنبيه
      await client.reply(m.chat, text || 'تنبيه هام من مطور البوت 📢', null, {
         mentions: users
      })
   },
   owner: true, // الميزة محمية وخاصة بمالك البوت فقط
   group: true  // الأمر يعمل حصرياً داخل المجموعات
}
