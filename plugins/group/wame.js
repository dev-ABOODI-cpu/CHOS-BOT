export const run = {
   usage: ['رابطي'], // الاسم العربي الرئيسي الذي سيظهر في القائمة (رابطي)
   hidden: ['wame', 'رابط_خاص'], // الاختصارات المخفية لضمان عمل البوت إذا كُتبت بالإنجليزية أو العربية البديلة
   category: 'المجموعات', // الفئة المعربة لتنظيم قائمة الأوامر
   async: async (m, {
      client,
      text
   }) => {
      // تحديد الرقم المستهدف: إما رقم صاحب الرسالة المقتبسة أو رقم مرسل الأمر الحالي
      let number = m.quoted ? (m.quoted.sender).split`@` [0] : (m.sender).split`@` [0]
      
      // تعيين نص افتراضي ترحيبي باللغة العربية في حال لم يكتب المستخدم نصاً مخصصاً بعد الأمر
      let chat = text ? text : 'مرحباً'
      
      // إرسال الرابط المولد مباشرة للمستخدم مع حفظ الحقوق
      client.reply(m.chat, `🔗 *رابط المحادثة المباشر:* \nhttps://wa.me/${number}?text=${encodeURI(chat)}\n\n© DEV ABOODI OFFICIAL`, m)
   },
   error: false
}
