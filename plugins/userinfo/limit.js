export const run = {
   usage: ['الحد'], // الاسم العربي الرئيسي الذي سيظهر في القائمة (الحد)
   hidden: ['limit', 'الليميت', 'نقاطي', 'محاولاتي'], // الاختصارات المخفية لضمان عمل البوت كبدائل دائماً
   category: 'معلومات المستخدم', // الفئة المعربة لتنظيم قائمة أوامر الأعضاء
   async: async (m, {
      client,
      isPrefix,
      users,
      Utils
   }) => {
      // 1. في حال استهلاك المستخدم لجميع النقاط المتاحة له اليوم
      if (users.limit < 1) {
         return client.reply(m.chat, `🚩 عذراً، لقد استهلكت جميع محاولاتك اليومية المتاحة لاستخدام البوت اليوم.\n\n🔄 سيتم تجديد الحد تلقائياً عند الساعة 00:00 منتصف الليل.\n\n⭐ للحصول على كمية نقاط غير محدودة ومزايا إضافية، يمكنك الاشتراك في النظام المميز عبر إرسال الأمر: *${isPrefix}مميز*`, m)
      }
      
      // 2. في حال وجود نقاط متبقية في حساب المستخدم
      let message = `🍟 رصيد محاولاتك الحالي : [ *${Utils.formatNumber(users.limit)}* ]`
      
      // إضافة اقتراح الترقية فقط إذا كان الحساب مجانياً وليس مميزاً مسبقاً
      if (!users.premium) {
         message += `\n\n⭐ للحصول على كمية نقاط غير محدودة ومزايا إضافية، يمكنك الاشتراك في النظام المميز عبر إرسال الأمر: *${isPrefix}مميز*\n\n© DEV ABOODI OFFICIAL`
      } else {
         message += `\n\n✨ أنت مشترك في النظام المميز حالياً.\n\n© DEV ABOODI OFFICIAL`
      }
      
      client.reply(m.chat, message, m)
   },
   error: false
}
